let base = 5
let altura = 9
const areaTriangulo = (base,altura) => base*altura/2
console.log(`El área de un triangulo de base ${base} y altura ${altura} es: ${areaTriangulo(base,altura)}`)

let radio = 7
const areaCirculo = radio => Math.PI * (radio * radio)
console.log(`El área de un círculo con radio ${radio} es: ${areaCirculo(radio)}`)
